import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const packageJsonPath = resolve(root, 'package.json');
const versionFilePath = resolve(root, 'src/app/app-version.ts');

const packageJson = JSON.parse(await readFile(packageJsonPath, 'utf8'));

if (typeof packageJson.version !== 'string' || packageJson.version.trim().length === 0) {
  throw new Error('package.json must define a non-empty version string.');
}

const escapedVersion = packageJson.version.replace(/\\/g, '\\\\').replace(/'/g, "\\'");

const output = `// Note: This file is generated by scripts/sync-app-version.mjs
export const appVersion = '${escapedVersion}';
`;

let existingOutput = '';

try {
  existingOutput = await readFile(versionFilePath, 'utf8');
} catch {
  // Note: The file will be created below
}

if (existingOutput !== output) {
  await mkdir(dirname(versionFilePath), { recursive: true });

  await writeFile(versionFilePath, output);
}
