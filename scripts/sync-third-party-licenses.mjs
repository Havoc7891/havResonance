import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const runtimePackages = [
  '@angular/common',
  '@angular/core',
  '@angular/forms',
  '@angular/platform-browser',
  '@angular/router',
  '@ngx-translate/core',
  '@ngx-translate/http-loader',
  'bootstrap-icons',
  'hash-wasm',
  'rxjs',
  'tslib',
];

const rootDirectory = process.cwd();
const outputPath = path.join(rootDirectory, 'public', 'third-party-licenses.txt');

const licenseFileCandidates = [
  'LICENSE',
  'LICENSE.md',
  'LICENSE.txt',
  'LICENCE',
  'LICENCE.md',
  'LICENCE.txt',
  'COPYING',
];

const licenseSections = await Promise.all(runtimePackages.map(readLicenseSection));

const notice = [
  'Third-party licenses for havResonance',
  '',
  'This file is generated by scripts/sync-third-party-licenses.mjs.',
  ...licenseSections,
  '',
].join('\n');

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, notice);

async function readLicenseSection(packageName) {
  const packageDirectory = path.join(rootDirectory, 'node_modules', packageName);
  const packageJsonPath = path.join(packageDirectory, 'package.json');
  const packageJson = JSON.parse(await readFile(packageJsonPath, 'utf8'));
  const licenseText = await readLicenseText(packageDirectory, packageName);

  return [
    '',
    '--------------------------------------------------------------------------------',
    `Package: ${packageName}`,
    `Version: ${packageJson.version ?? 'unknown'}`,
    `License: ${formatLicense(packageJson.license)}`,
    '',
    licenseText.trim(),
  ].join('\n');
}

async function readLicenseText(packageDirectory, packageName) {
  for (const candidate of licenseFileCandidates) {
    try {
      return await readFile(path.join(packageDirectory, candidate), 'utf8');
    } catch (error) {
      if (error?.code !== 'ENOENT') {
        throw error;
      }
    }
  }

  throw new Error(`Could not find a license file for ${packageName}.`);
}

function formatLicense(license) {
  if (typeof license === 'string') {
    return license;
  }

  if (license && typeof license === 'object' && typeof license.type === 'string') {
    return license.type;
  }

  return 'unknown';
}
